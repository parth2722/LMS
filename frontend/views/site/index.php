        <p>What is a learning management system (LMS)?
            A learning management system is a software or web-based technology used to plan, implement and assess a specific learning process. It's used for e-learning practices and, in its most common form, consists of two elements:
            a server that performs the base functionality and a user interface (UI) that is operated by instructors, students and administrators.
            Typically, an LMS provides an instructor with a way to create and deliver content, monitor student participation, and assess student performance. It might also provide students with interactive features,
            such as threaded discussions, video conferencing and discussion forums.
